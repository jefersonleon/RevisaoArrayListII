    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        if(tabela.getSelectedRow() != -1){
            i = tabela.getSelectedRow();
            JOptionPane.showMessageDialog(null, i);
            txtCodigo.setText(tabela.getValueAt(tabela.getSelectedRow(), 0).toString());
            txtNome.setText(tabela.getValueAt(tabela.getSelectedRow(), 1).toString());
            txtEmail.setText(tabela.getValueAt(tabela.getSelectedRow(), 2).toString());
            txtSenha.setText(tabela.getValueAt(tabela.getSelectedRow(), 3).toString());
            if(tabela.getValueAt(tabela.getSelectedRow(), 4).toString().equals("Feminino")){
                rbFem.setSelected(true);
            }else{
                rbMas.setSelected(true);
            }
        }
    }//GEN-LAST:event_tabelaMouseClicked
public void limparCampos(){
    txtCodigo.setText(null);
    txtNome.setText(null);
    txtEmail.setText(null);
    txtSenha.setText(null);
    rbFem.setSelected(true);
    
}
    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
       if(i!=-1){
          
           Usuario usu = new Usuario();
           usu.setId(Integer.parseInt(txtCodigo.getText()));
           usu.setNome(txtNome.getText());
           usu.setEmail(txtEmail.getText());
           usu.setSenha(txtSenha.getText());
           if(rbFem.isSelected()){
               usu.setSexo("Feminino");
           }else{
               usu.setSexo("Masculino");
           }
          todo.editar(i, usu);
          limparCampos();
           btPesquisarActionPerformed(evt);
       }else{
           JOptionPane.showMessageDialog(null, "Selecione um registro na tabela");
       }
    }//GEN-LAST:event_btEditarActionPerformed

