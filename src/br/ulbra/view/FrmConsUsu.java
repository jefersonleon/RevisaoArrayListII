    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
    if(todos.isSelected()){
        try {
            String matriz[][] = new String[CadUsu.bdUsu.size()][5];
            for (int i = 0; i < CadUsu.bdUsu.size(); i++) {
                matriz[i][0] = String.valueOf(CadUsu.bdUsu.get(i).getId());
                matriz[i][1] = CadUsu.bdUsu.get(i).getNome();
                matriz[i][2] = CadUsu.bdUsu.get(i).getEmail();
                matriz[i][3] = CadUsu.bdUsu.get(i).getSenha();
                matriz[i][4] = CadUsu.bdUsu.get(i).getSexo();
            }
            tabela.setModel(new javax.swing.table.DefaultTableModel(
                    matriz,
                    new String[]{
                        "id", "Nome", "Email", "Senha", "Sexo"
                    }
            ));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Sua base encontra-se vazia!!!");
        }
    }else if(cres.isSelected()){
             try {
            String matriz[][] = new String[CadUsu.bdUsu.size()][5];
            for (int i = 0; i < CadUsu.bdUsu.size(); i++) {
                matriz[i][0] = String.valueOf(CadUsu.bdUsu.get(i).getId());
                matriz[i][1] = CadUsu.bdUsu.get(i).getNome();
                matriz[i][2] = CadUsu.bdUsu.get(i).getEmail();
                matriz[i][3] = CadUsu.bdUsu.get(i).getSenha();
                matriz[i][4] = CadUsu.bdUsu.get(i).getSexo();
            }
           // Arrays.sort(matriz);
            tabela.setModel(new javax.swing.table.DefaultTableModel(
                    matriz,
                    new String[]{
                        "id", "Nome", "Email", "Senha", "Sexo"
                    }
            ));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Sua base encontra-se vazia!!!");
        }
    }else if(decres.isSelected()){
        JOptionPane.showMessageDialog(null, "Ordem Decrescente!!!");
    }else{
        JOptionPane.showMessageDialog(null, "Pesquisa por nome: "+txtListar.getText());
    }
        
    }//GEN-LAST:event_btPesquisarActionPerformed

